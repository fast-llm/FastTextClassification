MAX_VOCAB_SIZE = 10000  # 词表长度限制
UNK, PAD = '<UNK>', '<PAD>'  # 未知字，padding符号

logger = get_logger(__name__)

def build_vocab(file_path, tokenizer, max_size, min_freq):
    vocab_dic = {}
    with open(file_path, 'r', encoding='UTF-8') as f:
        for line in tqdm(f):
            lin = line.strip()
            if not lin:
                continue
            content = lin.split('\t')[0]
            for word in tokenizer(content):
                vocab_dic[word] = vocab_dic.get(word, 0) + 1
        vocab_list = sorted([_ for _ in vocab_dic.items() if _[1] >= min_freq], key=lambda x: x[1], reverse=True)[
                     :max_size]
        vocab_dic = {word_count[0]: idx for idx, word_count in enumerate(vocab_list)}
        vocab_dic.update({UNK: len(vocab_dic), PAD: len(vocab_dic) + 1})
    return vocab_dic

def get_pretrained_embeddings(pretrained_name_or_path, vectors=None, vocab=None,
                              cache='./data/pretrained_models/.vector_cache'):
    if vectors is None:
        vectors = Vectors(name=pretrained_name_or_path, cache=cache)
    if vocab is None:
        return vectors.vectors
    return vectors.get_vecs_by_tokens(list(vocab.keys()))


